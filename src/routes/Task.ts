import { Router } from "express";
import TaskService from "../services/Task";

// Init shared
const router = Router();

/******************************************************************************
 *                       Add One Task - "POST /api/tasks"
 ******************************************************************************/

router.post("/", TaskService.add);

/******************************************************************************
 *                       Update Task - "PUT /api/tasks"
 ******************************************************************************/

router.put("/:id", TaskService.update);

/******************************************************************************
 *                    Delete Task - "DELETE /api/tasks/:id"
 ******************************************************************************/

router.delete("/:id", TaskService.remove);

/******************************************************************************
 *                                     Export
 ******************************************************************************/

export default router;
